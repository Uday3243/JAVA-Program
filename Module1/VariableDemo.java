public class VariableDemo {
    public static void main(String[] args) {
        int year = 2025;
        double cgpa = 8.65;
        char section = 'C';

        System.out.println("Year: " + year);
        System.out.println("CGPA: " + cgpa);
        System.out.println("Section: " + section);
    }
}
